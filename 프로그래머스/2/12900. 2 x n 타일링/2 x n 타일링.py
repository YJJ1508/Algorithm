def solution(n):
    #규칙: 그전꺼 + 전꺼 = 현재값
    #2: [2] (-) 1+1 2
    #3: [3] (|||) (|=) (=|)  1+1+1 2+1 1+2
    #4: [5] ()
    
    d = [0]*(n+1)
    d[2] = 2
    d[3] = 3
    
    if n <= 3:
        return d[n]
    
    for i in range(4,n+1):
        d[i] = (d[i-1] + d[i-2]) % 1000000007
    
    return d[n]